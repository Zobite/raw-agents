export interface ScheduleState {
  days: number[];
  fromHour: number;
  toHour: number;
  intervalMinutes: number;
  startMessage: string;
}

export const DEFAULT_SCHEDULE: ScheduleState = {
  days: [],
  fromHour: 9,
  toHour: 18,
  intervalMinutes: 30,
  startMessage: "",
};

export function buildCron(s: ScheduleState): string {
  if (s.days.length === 0) return "";
  const dow = s.days
    .slice()
    .sort((a, b) => a - b)
    .join(",");
  if (s.intervalMinutes < 60) return `*/${s.intervalMinutes} ${s.fromHour}-${s.toHour} * * ${dow}`;
  const hi = s.intervalMinutes / 60;
  return hi === 1 ? `0 ${s.fromHour}-${s.toHour} * * ${dow}` : `0 ${s.fromHour}-${s.toHour}/${hi} * * ${dow}`;
}

/**
 * Parse a cron expression back into a ScheduleState.
 * Supports the formats generated by `buildCron`.
 */
export function parseCron(cron: string | null | undefined): ScheduleState {
  if (!cron?.trim()) return { ...DEFAULT_SCHEDULE };

  const parts = cron.trim().split(/\s+/);
  if (parts.length < 5) return { ...DEFAULT_SCHEDULE };

  const [minute, hour, , , dow] = parts;

  // Parse days
  const days = dow
    .split(",")
    .map(Number)
    .filter((n) => !Number.isNaN(n));

  // Parse hour range (e.g. "9-18" or "9-18/2")
  let fromHour = 9;
  let toHour = 18;
  const hourMatch = hour.match(/^(\d+)-(\d+)(?:\/(\d+))?$/);
  if (hourMatch) {
    fromHour = Number(hourMatch[1]);
    toHour = Number(hourMatch[2]);
  }

  // Parse interval
  let intervalMinutes = 30;
  const minuteStepMatch = minute.match(/^\*\/(\d+)$/);
  if (minuteStepMatch) {
    intervalMinutes = Number(minuteStepMatch[1]);
  } else if (minute === "0") {
    // Hourly or multi-hour
    const hourStep = hourMatch?.[3];
    intervalMinutes = hourStep ? Number(hourStep) * 60 : 60;
  }

  return { days, fromHour, toHour, intervalMinutes, startMessage: "" };
}

